	<h1 class="font-heading font-black mb-4 text-center text-3xl md:text-5xl">Karṇāṭaka Saṅgītam</h1>

	<p class="text-center" style="font-family: Tiro Telugu">నాదలోలుఁడై బ్రహ్మానందమందవే మనసా</p>
    <p>

    <p>
        Of all the arts, none captivates me so much as music. And what better musical tradition to study 
        than the one native to my ancestral lands, with kr̥ti-s offering just as much philosophical
        wisdom as they do rañjana? The veṇu, in particular, with its rich and beckoning tone will never cease to
        touch the very depths of my heart.  This is just a touch of what makes me appreciate the art of 
        karṇāṭaka saṅgītaṁ, or Carnatic music. I have also picked up the art of playing the vīṇa, for it
        facilitates an understanding of gamaka (the rich ornamentation characteristic of the art form) 
        like no other instrument.
    </p>
    <p>

    </p>
    <p>
        I must credit my study of the veṇu wholly for preserving my literacy in Tĕlugu. While it 
        is a recent phenomenon in the grand scheme of the art form, there is a rich structure to be seen through
        notation, and doing so in the medium of Tĕlugu script was an added benefit. And of course, as I've gotten older
        and am able to appreciate the compositions lyrically, they are a source of countless new words and aphorisms to learn.
    </p>
    <p>
        The power of music to bring people together is awe-inspiring, and with it I've made many connections through a shared love of Carnatic music (such as my friends at <a class="text-primary-500" href="https://www.instagram.com/curaaga/">Columbia 
            University Raaga</a>, the South Asian classical music club that I am co-president of), and for that I am eternally grateful.
    </p>
    
    <p>
        I have also begun a humble journey as a composer, both in Tĕlugu and Saṁskr̥taṁ.
    </p>
