    <div class="space-y-5">
        <p>
            This is my first kṣetra kr̥ti, composed on the kanakadurgā devasthāna on the banks of the kr̥ṣṇā river in vijayavāḍa (the sthalapurāṇa of the temple is where the city gets its name). This temple is particularly beloved and important to me and my family, and the sanctum of the temple, much like the name of the deity, has roofs made of gold that shine from a distance. The rāga rītigauḷa is a very melodic rakti rāga that evokes rasa-s of bhakti and karuṇa. Here is a rough translation:
        </p>  
        <div class="text-center">
            <p>Won't you come and protect me, O Kanakadurgā, the one who lives in a golden palace on the hill?</p>
            <p>You, who is worshipped by the best amongst earthly beings, the empress of Indrakīlādri,</p>
            <p>beloved by Malleśvara, protect me, the one who resides in the heart of the blazing-eyed (śiva)!</p>
            <p>Defender of the town of vijayavāḍa, slayer of mahiśāsura, just once</p>
            <p>won't you look upon this Veṅkaṭapraṇava, who adores you?</p>
        </div>

        <p>
            The Indrakīlādri hill is central to the sthalapurāṇa of this temple, where it is said Durgā manifested (svayambhū, or self-manifestation) to slay the demon Mahiśāsura (as mentioned in śākta texts such as the durgā saptaśatī). The temple itself is like a golden apex to the mountain, requiring a climb to get up to.
        </p>

        <p>I'd like to draw particular attention to the epithet ciccaṟakaṇṭihr̥dayavāsini, with ciccaṟakaṇṭi being one of my favorite tĕlugu epithets of Śiva. Literally translating to the one who resides in the heart of the one with the blazing eyes, I enjoy the fire-adjacent imagery that's evoked here (which, of course, is central to the ethos of Durgā, as seen in the famous Durgāsūkta of the Taittirī00ya āraṇyaka that extolls Durgā as agnivarṇāṁ tapasā jvalantīṁ, the one who is fiery-hued and blazes with the heat of tapas). And of course, what better color for describing fire than a bright, blazing gold? As kanakadurgā literally translates to golden Durgā. 
    </div>
