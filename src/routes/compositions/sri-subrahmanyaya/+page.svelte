    <div class="space-y-4">
        <p>The 56th rāga in the meḷakartāsaṅkhyā, ṣaṇmukhapriya literally means "beloved of ṣaṇmukha" (subrahmaṇya) and is the prati madhyama equivalent of naṭabhairavi. From the name, it is common to see compositions dedicated to both Subrahmaṇya and Gaṇapati in this rāga. This was the first kr̥ti of mine for where I was actually aware of the prāsa rules and also wanted to try putting a rāgamudra. I composed this by humming to myself while I was doing math research on the clock and scribbled it down in my work notebook beside my homology calculations. The summer of 2024, what interesting time that was! Enough of sounding like I'm not actually twenty (which I am, at the time of writing), here's a rough translation of the kr̥ti, which is in the caturthī vibhakti, or dative declension: </p>

        <div class="text-center">
            <p>
                I bow to the venerable Subrahmaṇya, the six-faced son of Agni.
            </p>
            <p>
                The one honored by thousands of the utmost beings, the one who bestows intellect, 
            </p>
            <p>husband of Devasenā, the young spear wielder,</p>
            <p>the son of Rudra born after Gaṇapati, riding a peacock, </p>
            <p>the protector of the one born under the stars beloved by Ṣaṇmukha!</p>
        </div>
        <p>The epithet prajñavivardhana is a reference to the prajñāvivardhana stotra from the rudrayāmala tantra, which is named after a now lost text (aside from quotations by other texts) that was profoundly influential on the āgama methods of worship. In general, the yāmala class of texts seem to predate the tantra-s. In said short stotra, containing twenty-eight names of subrahmaṇya, he is described as one who increases and develops the intellect of the chanter. The epithet śūlapāṇi, meaning spear wielder, is of course a reference to the tamiḻ phrase vĕṯṟivel murugā that one will commonly hear in temples. Overall, the kr̥ti introduces and describes the main facets of the deity.</p>
