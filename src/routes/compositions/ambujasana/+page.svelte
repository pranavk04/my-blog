    <div class="space-y-4">
        <p>Mohanakaḷyāṇi is another rāga that doesn't have too many compositions, but evokes such a sublime emotion of gentleness and beauty in the listener. This rāga was a post-trinity invention, said to be invented by Svāti Tiruṇāḷ with the composition seve śrīkāntam. Other compositions include the wonderful siddhivināyakaṁ seve'ham of Muttayya Bhāgavatar (as a flautist I would be remiss to not point you to this amazing recording by JB Sruthi Sagar <a class="underline text-primary-600 hover:text-primary-400" href="https://www.youtube.com/watch?v=RUrmBsVH7HU">here</a>) with its ciṭṭa svara passage and the beautiful tāmadam tagādayyā of Lālguḍi Gopāla Aiyyār (and the nice tillāna by his son). </p>

        <p>From the Vedic times immortal Lakṣmī Devī has been worshipped as the manifestation of good fortune, wealth, and happiness (as so succintly encapsulated by the beautiful śrī sūkta of the r̥gveda khilā: hiraṇyavarṇāṁ hariṇīṁ suvarṇa rajatasrajām). Upon thinking of the rāga mohanakaḷyāṇi and its beauty I immediately thought of a lotus blooming in water. And which goddess sits in a lotus other than Lakṣmī? Thus the above came flowing out. Here's a rough translation of the kr̥ti: </p>

        <div class="text-center">
            <p>One whose seat is borne of water, the one who sits on a lotus,</p>
            <p>does joyful prosperity exist without you?</p>
            <p>The one who bestows wealth and boons from her place in the sky,</p>
            <p>Mahālakṣmī, adored by Nārāyaṇa!</p>
            <p>The one with lovely splendour, of overwhelming rapturing beauty,</p>
            <p>praised by Veṅkaṭapraṇava, the golden one!</p>
        </div>

        <p>There is a nice śleṣa (pun) in the caraṇa, that has to do with the rāgamudra in place. The entire caraṇa if considered as one compound would contain mohanakaḷyāṇiveṅkaṭapraṇavanuta which could be taken as a tr̥tīya tatpuruṣa samāsa, that is to say, "praised by veṅkaṭapraṇava with mohanakaḷyāṇi." But the phrase mohanakalyāṇi is also a karmadhārya samāsa, simply meaning "bewitching beauty."  </p>
