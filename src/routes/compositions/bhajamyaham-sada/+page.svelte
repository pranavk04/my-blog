    <div class="space-y-4">
        <p>For those who are knowledgable in the Veda, the immediate parallels to and inspiration from the famous Durgāsūkta (the first Vedic passage I ever learned) will be immediately apparent. The rāga varāḷi here evokes a powerful and grand mood, which I think is accented by beginning the kr̥ti with a mahāprāṇākṣara (bhakāra) and the words agnivarṇāṁ (fire-hued). Here is a rough translation: </p>

        <div class="text-center">
            <p>I medidate always on Durgā, the fiery-hued.</p>
            <p>The protector of her true devotees, the one with blazing-eyes,</p>
            <p>beloved by the foremost deity Tryambaka, protect me, the one born of flames!</p>
            <p>Kātyāyanī, empress, praised by those who are noble, </p>
            <p>worshipped foremost every day by Veṅkaṭapraṇava, mother!</p>
        </div>
        <p>
            In the Taittirīya āraṇyaka (specifically in the mahānārāyaṇam), the only Vedic hymn to Durgā appears, where the goddess is identified with Agni-jātavedas, described to be born of fire and blazing with the heat of tapas. The same mantra-s appear in the r̥gveda-saṁhita but without the durgāgāyātrī (kātyāyanāya vidmahe'ti) and the mantra embodying the entire essence of the goddess: tāmagnivarṇāmiti. Encoding the essence of that particular mantra into a kr̥ti captures said essence for everyone, even those who might not know the sūkta itself.  
        </p>
    </div>
