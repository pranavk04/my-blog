    <div class="text-center">
        <h3>pallavi</h3>
        <p>
            ĕnni janmamula kuṇḍa maṇṭāvurā nanniṅkā  
        </p>
        <h3>anupallavi</h3>

        <p>
            anni janmamulaku nĕṟa nammina vāḍu kādā rāmacandra ninne 
        </p>

        <h3>caraṇa</h3>
        <p>
            maḷḷi yuṇḍamani palkite orvanurā raghurāma    
        </p>
        <p>
            vĕḷḷi vegame karuṇa jūḍarā yī <b>veṅkaṭapraṇavu</b>naku
        </p>
    </div>

    <div class="space-y-4">
        <p>
            his kr̥ti was composed the morning before some friends of mine performed toḍi as the main item of a kacceri. In that sense, the rāga itself was already in my head for the day. At the same time, I was also thinking about the tyāgarāju style of using tĕlugu words that are all related modifiers (ĕnni, anni, inni, ĕlā, ilā, alā, etc.) to create the required dvitīyākṣaraprāsa and also act in some sense as an anuprāsa. With that being said, it was quite spontaneous! Here is a rough translation:
         </p>
        <div class="text-center">
            <p>For how many more lifetimes will you make me wait?</p>
            <p>Me, who fully believed in you, Rāmacandra, for that many lifetimes?</p>
            <p>If you tell me I have to wait even longer I won't accept it, Raghurāma;</p>
            <p>Go quickly and show compassion to this Veṅkaṭapraṇava, (for how many more...)</p>
        </div>
        <p>
            The central theme of this kr̥ti is waiting. One can take the word "janmamulu" (lifetimes) to be both a hyperbolic expression of how long of a time it's taken but also as a reference to the broader concept of saṁsāra (reincarnation) and the ultimate goal of mokṣa (liberation). In this sense the anupallavi's statement of "anni janmamulaku nĕṟa nammina" (I believed in you for that many lifetimes) ties in that all of the actions of previous lives should be taken into account (which is the concept of karma on a broader scale) and emphasizes once again the vast period of time at hand. 
        </p>
    </div>
