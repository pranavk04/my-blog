    <div class="space-y-4">
        <p>
            The rāga kharaharapriya is said to be lifted from the sāman hymns of the udgātr̥ priests themselves, and a discerning ear will indeed note the seven svara chant of the kauthuma sāmavedin-s in the drāviḍa country indeed matches this description. Hence the rāga gains its initial name of harapriya, beloved by Śiva, who is said to thrill in the sāman chant (with the kaṭapayādi-saṁkhyā prefix khara denoting its position as the 22nd meḷakarta). While the number of svara-s in the other śākha-s (specifically, the rāṇāyanīya-s, for the jaiminī-s of Keraḷa maintain a unique and ancient method of chant) of the sāmaveda differs, those of the pañcadrāviḍa regions all fall under the meḷakarta (if we can consider the sāman chant within this framework) of what we now know as the grand rāga kharaharapriya. 
        </p>
        <p>
            Of the trinity, only tyāgarāju has composed in this rāga, with the govindadīkṣita sāṁpradāya (hence, the compositions of muddusvāmi dikṣita)having the avarohaṇa-dhaivata-varjya rudrapriya rāga instead. From tyāgarāju we have numerous masterpieces such as cakkani rājamārgamu, prakkala nilabaḍi, rāma nī samāna mĕvaru, and naḍaci naḍaci cūcĕra, to name a few. Particularly emphatic to me is the rendition of this rāga on the veṇu, with its contours and gamaka-s naturally fitting to my fingers with no effort (for, physically, the only partial shading required to render this rāga is on the gāndhāra svara, which is not difficult). For fitting by the name of the rāga itself, may this kr̥ti too be harapriya. Here is a rough translation (the  kr̥ti is in the dvitīyavibhakti, or accusative declension):  
        </p>

        <div class="text-center">
            <p>
                I worship Mahādeva, the one who loves the sāman chant, 
            </p>
            <p>
                who resides in my heart, who dwells in the mountains.
            </p>
            <p>
                Worshipped by the most eminent, the blue-necked one, 
            </p>
            <p>
                the beautiful, the destroyer of the yajña of Dakṣa-prajāpati!           
            </p>
            <p>
                The one praised by all the āgama-s and nigama-s, the three-eyed, 
            </p>
            <p>
                protector of the clan of Veṅkaṭapraṇava, Rudra!
            </p>
        </div>

        <p>
            The verb have is from the root (dhātu) hu, to sacrifice, offer, or worship (metaphorically through the initial meaning of offering oblations). Common to the south Indian śaiva temple and worship ethos is the namakaṁ chant of the taittirīya kr̥ṣṇa-yajurveda. Originally used during the agnicāyana soma sacrifice, the initial fire in the eagle altar is said to manifest rudra through its anger, only mollified by the namaka (śatarudrīya, as the equivalent is more often termed in the kāṇva school, where it forms the 16th adhyāya of the śukla yajurveda saṁhita) chant and all-giving with the camaka (vasordhārasūkta) chant. Interesting to note as an aside is the ghr̥tasūkta of the vājasaneyin-s that preceeds our vasordhāra chant. Instead of directly the famous vājaśca me prasavaśca me we have imamꣲ stanamūrjasvantan dhayāpāṁ prapīyam agne sarirasya madhye'ti. Following this the interesting ghr̥taṁ mimīkṣe ghr̥tamasya yonir ghr̥te śrito ghr̥tam̐ vvasya dhāmeti. 
        </p>
        <p>
            Anyways, one may recognize the epithets girīśanta and vāmadeva from the śatarudrīya.
        </p>
