        <p>
            The rāga śahana with its gentle gamaka-s and particularly the relationship between the catuśśruti dhaivata and kaiśiki niṣāda evokes the same feeling as sitting in the peaceful rays of the evening Sun does. But just like the Sun is both calm in the evening and grand during the morning sunrise, in the tāra sthāyi this rāga evokes a feeling of grandeur in the listener and especially with the characteristic iṟakkajāṟu gamaka-s (S-M, M-R) impart such rañjana to the listener. In my opinion, this rāga is most beautifully expressed on the veṇu (since the mūrcchana structure fits the physical quirks of the veṇu construction perfectly). 
        </p>
        <p>
            Of the six ṣaṇmata-s advocated for by ādi śaṅkarācārya, perhaps the saurya school has fallen the most to the wayside in the modern age. A pitiful matter, for Lord sūrya is one of the most extolled deities in the Vedasaṁhita-s, and is particularly dear to the vājasaneyin-s, or those who follow the śukla yajurveda schools (at the present day, the two extant śākha-s are the kāṇva-s, of whom I belong to, and the mādhyāndina-s). As is told in legend, the vājasaneya saṁhita is said to be revealed to its school's preceptor, yogīśvara Yājñavalkya maharṣi, by the Sun himself. There is a dedicated, extra sūryopāsana followed by vājasaneyin-s on top of the daily sūryopasthāna that involves chanting of the vibhrāḷiti anuvāka (the saura sūkta of the vājasaneyin-s), puruṣa sūkta, śiva saṅkalpa sūkta, and the manḍala brāhmaṇaṁ, a passage from the śatapatha brāhmaṇa. 
        </p>
        <p>
            Given this strong solar association of my ancestral school, it is only a given that I too contribute something. Here is a rough translation of the above kr̥ti, which is the longest kr̥ti I have composed to date, with the sāhitya flowing just like the Sun's rays and the passages of the rāga śahana.
        </p>
        <div class="text-center">
            <p>Sūryanārāyaṇa, nourish me with your shining rays, O foremost teacher of Yogīśvara!</p>
            <p>For the beginning of all rituals, rising early, you, O eminent one, are worshipped, </p>
            <p>the embodiment of Aryaman, Mitra, Varuṇa, Agni, and Āditya, you, who knows all births, </p>
            <p>protector of mortals, Parabrahmaṇa!</p>
            <p>Like your shining rays bestow a pleasant warmth on the entire world, </p>
            <p>with your flowing, purifying gaze, won't you come close to this</p>
            <p>Veṅkaṭapraṇava and show kindness upon me?</p>
        </div>

        <p>The name sūryanārāyaṇa in particular seems to be a very tĕlugu term of address (and of course, is the name of one of my ancestors), and one of the few remaining saura temples in the world is in āndhra, the arasavalli sūryanārāyaṇasvāmi vāru devasthānam in śrīkākuḷam, built by the kaḷinga king Devendra Varman in the 7th century AD.</p>

        <p>Sūrya is commonly identified with Agni in the Veda, including in the sūryopasthāna mantra udutyañ jātavedasan iti. Similarly, the concept of the Sun's gaze being purifying is also embodied by the Veda, such as in the pavamāna mantra brahma tena punīmahe ubhābhyāṁ devasavitaḥ. The identification of Agni and Sūrya is further complemented by the following mantra in the pavamāna passage: vaiśvānaro raśmibhirmā punātu (Vaiśvanara, purify me with your rays). The 6 āditya-s mentioned in the anupallavi are also always associated with solar motifs as well.</p>

        <p>The true form of the dvitīyākṣara prāsa is in display here, with the pallavi-anupallavi having the second syllable of each line be rya and ṟa for the caraṇa. For a grand and flowing rāga like śahana, it is only fitting that this composition too embody the same qualities. </p>
