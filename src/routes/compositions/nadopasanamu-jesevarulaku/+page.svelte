            సన్మార్గముఁ జూడవమ్మ నాకు
        </p>

        <h3>చరణం</h3>
        <p>
            చిత్తశుద్ధిలేక ప్రొద్దు వేగలేచి 
            మనసులో ననృతముఁ గల్గితే కర్మఁ 
            జేసి పుణ్యం బేమి వచ్చును
        </p>
        <p>
            మాఱుబల్కుమా యీ వేఙ్కటప్రణవును 
            బ్రొవుమా మాయమ్మ లోకజనని
        </p>
    </div>

    <div class="text-center">
