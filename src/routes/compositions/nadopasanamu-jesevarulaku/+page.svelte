        <p>phalitamu galadā mī yājñapti vinā o jagadambā</p>

        <h3>anupallavi</h3>
        <p>talli mī daya leka yemi ceyavalĕnu 
            sanmārgamu jūḍavamma nāku 
        </p>
        <h3>caraṇam</h3>
        <p>
