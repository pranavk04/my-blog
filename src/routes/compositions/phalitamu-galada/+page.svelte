        </div>

        <p>Here's my translation: O Vema! Without a pure soul, why follow ācāramu (proper customs and rituals)? Without a clean vessel, why cook in it? Without a pure mind, what is the use of worshipping Śiva?</p>

        <p>I learned this poem as a child, and it has stuck with me for my entire life, and is a question that I also deeply ponder myself.</p>
