<div class='space-y-4'>
	<h1 class="md:text-3xl font-shobhika text-3xl font-black text-center">Satatam ahaṁ smarāmi</h1>
	<p class="text-gray-700 text-center"> rāga: kāmavardhini, tāḷa: khaṇḍa cāpu.</p>

    <div class="text-center" style="font-family: Shobhika">
        <h3>पल्लवि</h3>
        <p>
            सततमहं स्मरामि तव नामं श्री वेङ्कटेशम्
        </p>
        <h3>अनुपल्लवि</h3>

        <p>
            सप्तगिरिवासिनं निजभक्तपोषकम् 
        </p>
        <p>
            कलियुगादिदेवं लक्ष्मीमनोरमणम्  
        </p>

        <h3>चरणम्</h3>
        <p>
            वेणुगानलोलं सर्वजगत्कारणम् 
        </p>
        <p>
            भार्गवमन्युहरणं <b>वेङ्कटप्रणव</b>पूजितम् 
        </p>
    </div>

    <div class="text-center">
        <h3>pallavi</h3>
        <p>satatam ahaṁ smarāmi tava nāmaṁ śrī veṅkaṭeśam</p>

        <h3>anupallavi</h3>
        <p>
            saptagirivāsinaṁ nijabhaktapoṣakam
        </p>
        <p>
            kaliyugādidevaṁ laksmīmanoramaṇam
        </p>
        <h3>caraṇam</h3>
        <p>veṇugānalolaṁ sarvajagatkāraṇam
        </p>
        <p>
            bhārgavamanyuharaṇaṁ <b>veṅkatapraṇava</b>pūjitam
        </p>
    </div>

    <div class="space-y-4">
        <p>This was the first kr̥ti I ever composed, on my kuladaivaṁ, śrī veṅkaṭeśvara svāmi. In that sense, it functions as an offering and a benedictment that as a vāggeyakāra I would continue to compose more and more. The rāga kāmavardhini (also known as pantuvarāḷi) has many dimensions, and here a more peaceful and calm aspect of the rāga is embodied. It offers a good scope for musical variety while also being very plesant to listen to. The kr̥ti is in the dvitīyavibhakti (accusative declension) and includes references to other incarnations, or avatāra-s, of Viṣṇu. Here is a rough translation: </p>

        <div class="text-center">
            <p>I meditate always on your name, Śrī Veṅkaṭeśa!</p>
            <p>The one who lives upon the seven hills, beloved of Laksmī!</p>
            <p>The foremost diety of the Kali age, the one who causes all in the universe!</p>
            <p>Destroyer of the anger of Pārśurāma, worshipped by Veṅkaṭapraṇava!</p>
        </div>
        <p>
            While I was not conscious of the fact completely (as can be seen from the caraṇa), the sāhitya here coincidentally ended up following dvitīyākṣara prāsa. The medium being saṁskr̥ta is also a nice parallel to that of tyāgarāja svāmi (who also composed his first kr̥ti in saṁskr̥ta, namo namo rāghavāya in deśika toḍi, and who I am a sixth-generation disciple of). 
        </p>
        <p>As I have since made practice for the first kr̥ti I compose in any rāga, this kr̥ti has a ciṭṭasvara passage.</p>
    </div>
