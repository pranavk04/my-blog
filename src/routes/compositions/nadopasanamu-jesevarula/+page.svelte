    <div>
        <p>
            Bilahari is one of the happiest and most beautiful rāga-s. A bhāṣāṅga janya of śaṅkarābharaṇaṁ, it admits the kaiśiki niṣāda svara in the phrases, PDND and PNDP. One can hint at the kaiśiki niṣāda when rendering subtle kaṁpita on the dhaivata svara to give the rāga extra beauty, but the essence of this rāga can be conveyed completely without the anyasvaraprayoga-s at all. Bilahari overtook its inspirational parent deśākṣi, which was limited in its sthāyi scope and was a upāṅga janya of harikāṁbhoji. 
        </p>
        <p>What better rāga to convey the message that saṅgīta provides the ultimate bliss? Here's a rough translation of the kr̥ti:</p>
        <div class="text-center">
            <p>Does there exist happiness greater than those who embody music?</p>
            <p>Without the faultless and shining seven svara-s,</p>
            <p>can the mind truly obtain the ultimate bliss?</p>
            <p>Those who know the Veda-s and Śāstra-s know,</p>
            <p>and this is understood too by Veṅkatapraṇava!</p>
        </div>

        <div class="space-y-4">
            <p>
                I truly love music. It's one of the things that instantly brightens my day regardless of what is happening, and has given me so many invaluable and deep connections in life to people, my culture, and myself. And I wanted to convey this heartfelt message through my favorite medium, music! To take it literally, nādopāsanamu (from the Sanskrit words nāda and upāsana, meaning worship or adoration through music) implies religious character, but I don't want to limit the message to the religious meaning of upāsana. One can worship music for the sake of music itself, for adoration of music is a common human idea, theme, and practice. 
            </p>
            <p>
                The caraṇa is not a baseless statement either. Music is absolutely praised to no end by the Śāstra-s and Veda-s (for of course, we have an entire Veda of songs, the sāmaveda). The vīṇa, veṇu (flute) and voice are praised as the three divine instruments, along with numerous percussional instruments of various forms. I would like to quote the Yājñavalkya dharmaśāstra (the Yājñavalkyasmr̥ti, the dharmaśāstra text of the vājasaneyin-s), which so wonderfully says 
            </p>
            <div style="font-family: Shobhika" class="text-center">
                <p>वीणावादन तत्त्वज्ञश्श्रुतिजातिविशारदः। </p>
                <p>  तालज्ञश्चाप्रयासेन मोक्षमार्गं नियच्छति॥ </p>
            </div>
            <p>Here's my translation of the above: "One who understands the character of playing the vīṇa surely understands the true character of the śruti (the Veda). One who knows the nature of tāḷa easily obtains the the path of liberation." If music wasn't so highly valued, why would it be something that liberates the soul from the cycle of saṁsāra? At the end of the day, the creative and fine arts, whether they be saṅgīta, śilpa (sculpture), nāṭya, or anything in between, are the true aspirations of the human experience, and it's important to reflect on that. </p>
         </div>
