    <div class="text-center">
        <h3>pallavi</h3>
        <p>rāra rāmabhadra nannu dalagācuniki</p>

        <h3>anupallavi</h3>
        <p>dhīradhanurdhara daśagrīvajaitra  
        </p>
        <p>
            varasadguṇādipuruṣa sāketapurapālaka
        </p>
        <h3>caraṇam</h3>
        <p>
            layarāgatāḷa ganaka
        </p>
        <p>
           daya jūḍu <b>veṅkaṭapraṇavu</b>ku
        </p>
    </div>

    <div class="space-y-4">
        <p>This kr̥ti came to me while I was in the elevator in my dorm building. It embodies the more energetic and awe-inspiring feeling of sāveri rāga that comes out with a faster paced kr̥ti. I also wanted to compose something in rūpaka tāḷa. Here's a rough translation:</p>

        <div class="text-center">
            <p>Won't you come, Rāmabhadra, to protect me?</p>
            <p>Valorous wielder of the bow, defeater of the ten-headed one, </p>
            <p>the eminent one who has all good qualities, protector of Sāketapura!</p>
            <p>Because of this rhythm, rāga, and tāḷa, show kindness to Veṅkaṭapraṇava, (for...)</p>
        </div>

        <p>There is a nice example of anuprāsa, or alliteration, in the anupallavi. The repetition of the dhakāra in <i>dhī</i>ra<i>dha</i>nur<i>dha</i>ra is the first time I was able to use anuprāsa. The name Sāketa for Ayodhya seems to be a medieval term that is quite popular in the southern regions of India. The word daśagrīvajaitr̥ is of course refering to Rāma's defeat of Rāvaṇāsura. The other qualities of Rāma, such as his depiction as the maryādapuruṣa and his excellence at archery are also embodied by this shorter kr̥ti. </p>
    </div>
